public class BinaryAND {
    public static void main(String[] args) {
        int a = 5, b = 6;

        int BinaryAND = a & b;
        System.out.println("Binary {AND} of a = 5 , b = 6 is :" + BinaryAND);

        int add = a + b;
        System.out.println("\nAddition of a = 5, b = 6 is :" + add);
    }
}
/*
 * OUTPUT:=
 * 
 * Binary {AND} of a = 5 , b = 6 is :4
 * 
 * Addition of a = 5, b = 6 is :11
 */