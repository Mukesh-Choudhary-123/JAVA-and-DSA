public class BinaryXOR {
    public static void main(String[] args) {
        int a = 5, b = 6;

        int BinaryXOR = a ^ b;
        System.out.println("Binary {OR} of a = 5 , b = 6 is :" + BinaryXOR);

        int add = a + b;
        System.out.println("\nAddition of a = 5, b = 6 is :" + add);
    }
}
/*
 * OUTPUT:=
 * 
 * Binary {OR} of a = 5 , b = 6 is :3
 * 
 * Addition of a = 5, b = 6 is :11
 */