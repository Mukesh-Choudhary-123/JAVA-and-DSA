public class BinaryOR {
    public static void main(String[] args) {
        int a = 5, b = 6;

        int BinaryOR = a | b;
        System.out.println("Binary {OR} of a = 5 , b = 6 is :" + BinaryOR);

        int add = a + b;
        System.out.println("\nAddition of a = 5, b = 6 is :" + add);
    }
}
/*
 * OUTPUT:=
 * Binary {OR} of a = 5 , b = 6 is :7
 * 
 * Addition of a = 5, b = 6 is :11
 * 
 */