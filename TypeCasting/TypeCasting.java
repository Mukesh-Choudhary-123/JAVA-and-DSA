public class TypeCasting {

    public static void main(String[] args) {

        // Type Casting float to int
        float a = 25.567f;
        int b = (int) a;
        System.out.println("value of a(float) befor type casting = " + a);
        System.out.println("value of b(int) after type casting = " + b);

        // Type Casting char to int
        char ch = 'a';
        int num = (int) ch;
        System.out.println("value of ch(char) befor type casting = " + ch);
        System.out.println("value of num(int) after type casting = " + num);
    }
}